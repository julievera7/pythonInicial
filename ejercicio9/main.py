import modulo

def main():
	rsuma = modulo.suma(10,2)
	print("Resultado de la suma: ", rsuma)
	rresta = modulo.resta(10,2)
	print("Resultado de la resta: ", rresta)
	rproducto = modulo.producto(10,2)
	print("Resultado de la multiplicación: ", rproducto)
	rdivision = modulo.division(10,2)
	print("Resultado de la división: ", rdivision)
	
if __name__ == '__main__':
	main()
